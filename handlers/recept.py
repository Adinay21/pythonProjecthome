                "Плавленый сыр (если в виде брусочка) натереть на терке или порезать кубиками. "
                "В кипящий бульон добавить картофель. С момента закипания 5–7 минут. В это время сделать "
                "слабую зажарку на сливочном масле. Сначала положить лук, затем морковь. Слегка посолить и поперчить."
