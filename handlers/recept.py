from aiogram import Router, types
from aiogram.filters import Command



recept_router = Router()

@recept_router.message(Command("recept"))
async def picture_handler_1(message: types.Message):
    photo = types.FSInputFile("boloneze.jpeg")
    await message.answer_photo(
        photo=photo,
        caption="Разогрейте оливковое масло в большой сковороде и добавьте измельченные чеснок, лук, морковь и "
                "сельдерей. Тушите на маленьком огне 5 минут до мягкости овощей. Увеличьте огонь и добавьте мясо. "
                "Перемешайте деревянной лопаткой, разломав комочки. Добавьте бульон, вино, помидоры, сахар и петрушку."
                " Доведите до кипения, уменьшите огонь и тушите 1,5 часа, иногда помешивая. Посолите и добавьте специи."
                " Перед подачей сварите спагетти, слейте и подавайте с соусом и пармезаном."
    )


